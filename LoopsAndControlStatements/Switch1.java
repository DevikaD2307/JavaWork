package LoopsAndControlStatements;

public class Switch1
{
    public static void main(String[] args)
    {
        int x=2;
        switch (x)
        {
            case 1:
                System.out.println("It is cool");
                break;
            case 2:  System.out.println("it is hot");
                break;
            case 3:
                System.out.println("is is rainy");
                break;
            default:
                System.out.println("none of the above");


        }
    }
}

