package ArraysAndStrings;

public class Arr3
{
    public static void main(String[] args)
    {
        // printing reverse order using array
        int arr[]= new int [] {10,20,30,40,50};
        for (int i= arr.length-1;i>=0;i--)
        {
            System.out.println(arr[i]);
        }

    }
}
